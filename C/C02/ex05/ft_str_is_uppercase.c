int	ft_str_is_uppercase(char *str)
{
	int	i;
	int	a;

	i = 0;
	a = 0;
	while (str[i])
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			a++;
		}
		i++;
	}
	if (str[0] != '\0')
	{
		if (a == i)
		{
			return (1);
		}
		else
		{
			return (0);
		}
	}
	return (1);
}
