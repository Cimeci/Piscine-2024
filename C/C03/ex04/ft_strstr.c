char	*ft_strstr(char *str, char *to_find)
{
	int	a;
	int	i;

	i = 0;
	while (str[i])
	{
		while (str[i + a] == to_find[a])
		{
			a++;
			if (to_find[a] == '\0')
				return (str + i);
		}
		a = 0;
		i++;
	}
	return (0);
}
