#include <unistd.h>

void	ft_putstr(char *str)
{
	int	a;

	a = 0;
	while (str[a])
	{
		write(1, &str[a], 1);
		a++;
	}
	write(1, "\n", 1);
}

int	main(int argc, char **argv)
{
	ft_putstr(argv[0]);
	return (0);
}
